struct UFDS {
    vi p;
    int sets;

    UFDS(int n) {
        p.reserve(n);
        sets = n;
        for (int i = 0; i < n; i++)p.push_back(i);
    }

    int find(int n) {
        if (n == p[n])return n;
        return p[n] = find(p[n]);
    }

    inline bool sameset(int x, int y) { return find(x) == find(y); }

    inline int unionset(int x, int y) {
        sets--;
        x = find(x);
        y = find(y);
        return p[y] = x;
    }
};

inline void solve() {
    ll n, x, y;
    int k, q;
    cin >> n >> k >> q;
    if (k == 1) {
        while (q--) {
            cin >> x >> y;
            pnl(abs(x - y));
        }
        return;
    }
    while (q--) {
        cin >> x >> y;
        int res = 0;
        while (x != y) {
            if (y > x)swap(x, y);
            x = (x + k - 2) / k;
            res++;
        }
        pnl(res);
    }
}


inline int euclid(int a, int b, int &x, int &y) { // pass x and y by ref
    int xx = y = 0;
    int yy = x = 1;
    while (b) { // repeats until b == 0
        int q = a / b;
        int t = b;
        b = a % b;
        a = t;
        t = xx;
        xx = x - q * xx;
        x = t;
        t = yy;
        yy = y - q * yy;
        y = t;
    }
    return a; // returns gcd(a, b)
}

inline ll binpow(ll a, int p, int m) {
    ll res = 1;
    while (p) {
        if (p & 1)res = (res * a) % m;
        a = (a * a) % m;
        p >>= 1;
    }
    return res;
}

inline double f(int x, int y, int k) {
    double t = sqrt((k - y) / 5.0);
    return x / t;
}

inline pdd g(int x1, int y1, int x2, int y2, int k) {
    double m = (y2 - y1) / (double) (x2 - x1);
    double c = y2 - m * x2;
    if (k > c)return pair(inf, 0);
    double v = sqrt(20 * (c - k)) / abs(m);
    double x = -v * v * m / 10;
    return pair(x, v);
}


inline void solve() {
    int n;
    cin >> n;
    if (n == 1) {
        cout << 1;
        return;
    }
    n--;
    int middle = 0;
    // 11
    // 101,111
    // 1001,1111
    // 10001,10101,11011,11111
    // 100001,101101,110011,111111
    // 1000001...
    for (int i = 1;; i++) {
        n--;
        if (n == 0) {
            string s;
            while (i) {
                s.pb('0' + i % 2);
                i >>= 1;
            }
            reverse(all(s));
            string res = s;
            if (middle == 1)res.pb('0');
            else if (middle == 2)res.pb('1');
            reverse(all(s));
            res.append(s);
            cout << stoi(res, nullptr, 2);
            return;
        }
        if (middle == 1) {
            middle = 2;
            i--;
            continue;
        } else if (middle == 2)middle = 1;
        if (popcount(i + 1) == 1) {
            if (middle == 0) {
                i = ((i + 1) >> 1) - 1;
                middle = 1;
            } else middle = 0;
        }
    }
}


